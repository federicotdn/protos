package proxy;

public class POP3ServerConfig {
    
    private String defaultPOP3Host;
    private int defaultPOP3Port;
    
    public String getDefaultPOP3Host() {
        return defaultPOP3Host;
    }

    public void setDefaultPOP3Host(String defaultPOP3Host) {
        this.defaultPOP3Host = defaultPOP3Host;
    }

    public int getDefaultPOP3Port() {
        return defaultPOP3Port;
    }

    public void setDefaultPOP3Port(int defaultPOP3Port) {
        this.defaultPOP3Port = defaultPOP3Port;
    }
}
